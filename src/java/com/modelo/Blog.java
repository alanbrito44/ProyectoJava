package com.modelo;

/**
 *
 * @author dougl
 */
public class Blog {
    int idBlog;
    int idUser;

    public Blog() {
    }

    public Blog(int idBlog, int idUser) {
        this.idBlog = idBlog;
        this.idUser = idUser;
    }

    public int getIdBlog() {
        return idBlog;
    }

    public void setIdBlog(int idBlog) {
        this.idBlog = idBlog;
    }

    public int getIdUser() {
        return idUser;
    }

    public void setIdUser(int idUser) {
        this.idUser = idUser;
    }
    
    
}
